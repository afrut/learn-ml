# The hypothesis testing framework makes assertions regarding the
# population using sample statistics, one of which is the sample mean.
# The sample mean varies around the population mean. Assertions
# made through hypothesis testing carry a certian level of uncertainty
# as quantified by the significance level, alpha.
#
# Alpha quantifies the probability of rejecting H0 when it is in fact
# true. This is known as a Type I error. A Type II error is failing
# to reject H0 when the alternative hypothesis is, in fact, true.
#
